# x is an integer
x = 10
print(type(x))
# a is a string
a = "john"
print(type(a))
# b is a float
b = 3.14
z = 5e3
print(z)
print(type(b))
# c is a set
c = {"orange", "banana", "apple"}
print(type(c))
# d is a complex variable
d = 5j + 3
print(type(d))
print(d)
# e is a dict
e = {"state" : "Edo", "city" : "benin" , "number": 12} 
print(type(e))
# f is a range
f = range (6)
print(type(f))
g = False
print(type(g))
# j is a memoryview
j = memoryview(bytes(8))
print(type(j))
print(j)
# casting of data types 
y = str("s1")
print(y)
r = float(3.5)
print(r)
